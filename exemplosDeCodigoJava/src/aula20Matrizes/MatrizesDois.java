package aula20Matrizes;

public class MatrizesDois {
    public static void main(String[] args) {

        // Exemplo de criação de uma matriz multi-dimensionais;
        // linhas, colunas
        // alunos - notas
                                        // aluno - nota
        double [][] numeros = new double[4][5];

        numeros[0][0] = 1;
        numeros[0][1] = 3;
        numeros[0][2] = 5;
        numeros[0][3] = 7;
        numeros[0][4] = 9;

        numeros[1][0] = 4;
        numeros[1][1] = 5;
        numeros[1][2] = 5;
        numeros[1][3] = 3;
        numeros[1][4] = 8;

        numeros[2][0] = 5;
        numeros[2][1] = 5;
        numeros[2][2] = 5.6;
        numeros[2][3] = 7;
        numeros[2][4] = 6;

        numeros[3][0] = 4.5;
        numeros[3][1] = 7.8;
        numeros[3][2] = 8;
        numeros[3][3] = 9;
        numeros[3][4] = 7;




    }
}
